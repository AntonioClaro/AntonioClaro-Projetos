    public String calculateTime(int[] a,int[] b){
        int h,m,s;
        int a2 = a[2], a1 = a[1], a0 = a[0];
        int b2 = b[2], b1 = b[1], b0 = b[0];
        
        if(b[2]<a[2]){
            s = 60-(a2-b2);
            b1-=1;
        }
        else{
            s = b2-a2;
        }
        
        if(b[1]<a[1]){
            m = 60-(a1-b1);
            b0-=1;
        }
        else{
            m = b1-a1;
        }
        
        h = b0-a0;
        
        if(h<1){
            return "01:00:00";
        }
        
        return String.valueOf(Math.abs(h))+":"+String.valueOf(m)+":"+String.valueOf(s);
        

    }
    
    public void setExit(int a1, String a2, String a3, String a4) {
        Date time = new java.util.Date(System.currentTimeMillis());
        String t = new SimpleDateFormat("HH:mm:ss").format(time);
        int[] before = new int[3];
        int[] after = new int[3];
        int[] value = new int[3];
        String l;
        String v;
        for(int i=0;i<3;i++){
            before[i] = Integer.parseInt(a4.split(":")[i]);
            after[i] = Integer.parseInt(t.split(":")[i]);
        }
        
        l = calculateTime(before,after);
        
        for(int i=0;i<3;i++){
            value[i] = Integer.parseInt(l.split(":")[i]);
        }
        
        v = String.valueOf(con.Value(value));
        
        //System.out.println(l);

        id=a1;
        jTextField1.setText(a2);
        jTextField2.setText(a3);
        jTextField3.setText(a4);
        jTextField4.setText(t);
        jTextField5.setText(l);
        jTextField6.setText(v);
    }
    
